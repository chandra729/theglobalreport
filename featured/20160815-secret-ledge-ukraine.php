<h2>Secret Ledger in Ukraine Lists Cash for Donald Trump’s Campaign Chief</h2>
       
<p> On a leafy side street off Independence Square in Kiev is an office used for years by Donald J. Trump’s campaign chairman, Paul Manafort, when he consulted for Ukraine’s ruling political party. His furniture and personal items were still there <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login"> as recently as May.</a></p>
        
<p><img class="alignright" src="/images/ukraine.png " width="300" height="300" alt="pro-Russian political party of Viktor F. Yanukovych"/></p>

<p>And Mr. Manafort’s presence remains elsewhere here in the capital, where government investigators examining secret records have found his name, as well as companies he sought business with, as they try to untangle a corrupt network they say was used to loot Ukrainian assets and influence elections during the administration of Mr. Manafort’s main client, former President Viktor F. Yanukovych.
</p>

<p> Handwritten ledgers show $12.7 million in undisclosed cash payments designated for Mr. Manafort from Mr. Yanukovych’s pro-Russian political party from 2007 to 2012, according to Ukraine’s newly formed National Anti-Corruption Bureau. Investigators assert that the disbursements were part of an illegal off-the-books system whose recipients also included election officials.</p>


<p> In addition, criminal prosecutors are investigating a group of offshore shell companies that helped members of Mr. Yanukovych’s inner circle finance their lavish lifestyles, including a palatial presidential residence with a private zoo, golf course and tennis court. Among the hundreds of murky transactions these companies engaged in was an $18 million deal to sell Ukrainian cable television assets to a partnership put together by Mr. Manafort and a Russian oligarch, Oleg Deripaska, a close ally of President Vladimir V. Putin.</p>


<p>Mr. Manafort’s involvement with moneyed interests in Russia and Ukraine had previously come to light. But as American relationships there become a rising issue in the presidential campaign — from Mr. Trump’s favorable statements about Mr. Putin and his annexation of Crimea to the suspected Russian hacking of Democrats’ emails — an examination of Mr. Manafort’s activities offers new details of how he mixed politics and business out of public view and benefited from powerful interests now under scrutiny by the new government in Kiev.
</p>


<p>Anti-corruption officials there say the payments earmarked for Mr. Manafort, previously unreported, are a focus of their investigation, though they have yet to determine if he actually received the cash. While Mr. Manafort is not a target in the separate inquiry of offshore activities, prosecutors say he must have realized the implications of his financial dealings.</p>


<p>“He understood what was happening in Ukraine,” said Vitaliy Kasko, a former senior official with the general prosecutor’s office in Kiev. “It would have to be clear to any reasonable person that the Yanukovych clan, when it came to power, was engaged in corruption.”

Mr. Kasko added, “It’s impossible to imagine a person would look at this and think, ‘Everything is all right.’” </p>

<p>
Mr. Manafort did not respond to interview requests or written questions from The New York Times. But his lawyer, Richard A. Hibey, said Mr. Manafort had not received “any such cash payments” described by the anti-corruption officials.
</p>	


<p>Mr. Hibey also disputed Mr. Kasko’s suggestion that Mr. Manafort might have countenanced corruption or been involved with people who took part in illegal activities.

“These are suspicions, and probably heavily politically tinged ones,” said Mr. Hibey, a member of the Washington law firm Miller & Chevalier. “It is difficult to respect any kind of allegation of the sort being made here to smear someone when there is no proof and we deny there ever could be such proof.” </p>	

