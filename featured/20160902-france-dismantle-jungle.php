<h2>France vows to dismantle 'Jungle' refugee camp in Calais </h2>

<p class="details">By Angelique Chrisafis  Friday 2 September 2016 08.09 BST <a href="Theguardian"></a> </p>
<p>
<blockquote><b>Interior minister says France will create accommodation for thousands of refugees elsewhere  <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank"> in France ‘to unblock Calais’. </a> </b></blockquote> </p>


<p><img class="alignright" src="/images/france.png" width="350" height="350" alt="France" /></p>


<p>
France is to gradually dismantle the  <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> “Jungle” refugee camp in Calais </a>, the interior minister, Bernard Cazeneuve, has vowed.
</p> <p>
Cazeneuve told the regional newspaper the Nord Littoral he would press ahead with the closure of the camp “with the greatest determination”, dismantling the site in stages, clearing the former wasteland where record numbers of refugees and migrants are sleeping rough in dire sanitary conditions as many hope to reach Britain.
</p> <p>
He said France would create accommodation for thousands elsewhere in the country “to unblock Calais”.
</p> <p>
French authorities have made repeated efforts to shut down the camp, which the state was responsible for creating in April 2015 when authorities evicted migrants and refugees from squats and outdoor camps across the Calais area and concentrated them into one patch of <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">  wasteland without shelter.</a> 
</p> <p>
French authorities have made repeated efforts to shut down the camp, which the state was responsible for creating in April 2015 when authorities evicted migrants and refugees from squats and outdoor camps across the Calais area and concentrated them into one patch of wasteland without shelter. 
</p> <p>
Less than six months ago, the authorities demolished a large area of the southern part of the camp, saying the aim was to radically reduce numbers. But this month the number of people in the camp reached an all-time high of almost 10,000 people, aid organisations estimate.
</p> <p>
The French authorities put the official number of people in the camp at almost 7,000. Authorities have said over the past year more than 5,000 asylum seekers have left the northern French town for  <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" > 161 special centres set up around France. </a>
</p> <p>
Cazeneuve said places for another 8,000 asylum seekers would be created this year and thousands more in 2017, saying efforts would be focused on getting peole in Calais to leave voluntarily.
</p> <p>
Currently a record 1,900 French police are operating in Calais, and Cazeneuve said another 200 would be added to their ranks “to reinforce the battle” against migrants smuggling themselves on to <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> lorries bound for Britain.</a>
</p> <p>
Daniel Barney, of Médecins Sans Frontières (MSF), which opened a health centre in the camp, warned last week that the French and UK governments were turning a blind eye to the growing problems.
</p> <p>
He said the French authorities’ decision to demolish the southern part of the camp in March had made the situation worse. <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> “Half the camp was dismantled </a>. So now we have double the population living in half as much land, with access to the same amount of water points and toilets. There is an extreme problem of overcrowding. Conditions in the camp are getting progressively worse.”
</p> <p>

With overcrowding, poor sanitary conditions, food shortages and a rise in the number of violent attacks on lorry drivers heading to the UK, there is growing tension in Calais and politicians from all parties are seizing on the seemingly intractable problem of how to deal with refugees and migrants trapped in France hoping to reach England. In the run-up to the <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> French presidential election </a> next year, the French right and far-right have increased their calls for hardline action on Calais.
</p> <p>
Cazeneuve will visit Calais on Friday afternoon, as French lorry drivers, shopkeepers and farmers plan to stage a <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> blockade of the port </a> on Monday to demand the camp is demolished.
</p> <p>
The Socialist president, François Hollande, who has until now avoided <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank"> visiting Calais, </a> is to visit the city later this month.

</p>

