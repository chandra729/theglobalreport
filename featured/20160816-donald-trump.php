<h2>Donald Trump proposes TITMUS test for visitors to US from terror-prone areas</h2>
       

        
<p> Attention prospective immigrants and visitors to the United States, Muslims in particular — stand-by to fill in the TITMUS form <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login">if you wish to enter America under a Trump administration.</a></p>

<p>
In a sweeping but still sketchy containment plan purportedly aimed at checking terrorism, Republican presidential candidate Donald Trump on Monday proposed an ''ideological test,'' which he described as part of an ''extreme vetting'' process, that would filter out from the immigration process those who do not believe in the US Constitution and American principles and values. 
</p>
        
<p><img class="alignright" src="/images/donald.jpeg" width="250" height="250" alt="trump" /></p>';

<p>
''In the Cold War, we had an ideological screening test. The time is overdue to develop a new screening test for the threats we face today,'' Trump said in a national security speech in Youngstown, Ohio while announcing proposals to develop a new immigration paradigm. He indicated that the Trump Ideological Test (TIT) would be directed mainly at MUSlims from the Middle East, but left open the possibility that other regions could also be covered by it while proposing an omnibus screening of anyone who not consonant with American values or ideals as determined by him. 
</p>

<p>
It was also not clear which of these proposals pertained only to prospective immigrants and which of them applied to visitors and short-term visa applicants, including tourists and business people.
</p>


<p>''A Trump administration will establish a clear principle that will govern all decisions pertaining to immigration: we should only admit into this country those who share our values and respect our people,'' the New York-born candidate, whose grandparents emigrated from Germany, said in one portion of the speech that seemed to be aimed at only potential immigrants. "In addition to screening out all members or sympathizers of terrorist groups, we must also screen out any who have hostile attitudes towards our country or its principles - or who believe that Sharia law should supplant American law," he added in what seemed to be a broader dragnet to keep out even visitors deemed to be radical or dangerous. 
</p>


<p>Immigration experts and constitution doyens immediately saw plenty of problems in the Trump plans and their execution although the campaign indicated the proposals are still a work in progress.
</p>


<p>
For instance, Trump said, ''Those who do not believe in our Constitution, or who support bigotry and hatred, will not be admitted for immigration into the country.'' Many native-born Americans have issues with parts of the US Constitution (particularly the Second Amendment - the right to bear arms - that is dear to Trump supporters). And, according to liberals, many Trump supporters are themselves steeped in bigotry and hatred - towards gays and minorities. The Trump campaign also draws support from many radical and extremist organizations such as the Ku Klux Klan. 
</p>



<p>
''Why focus on immigrants? Why not follow Trump's mantra of 'America First' and apply this test to American citizens? ... Because if the test could be applied to natural-born citizens, the result would be that over half of the Republican Party — and most of Trump's voters — would be banned from the United States,'' wrote Dean Obeidallah, a radio host who has co-directed the comedy documentary ''The Muslims Are Coming!''
</p>

<p>
"Since Donald Trump wants to impose new tests on immigrants, he should take the one test every immigrant has to pass to become a United States citizen. He would almost certainly fail, given his general ignorance and weak grasp of basic facts about American history, principles and functioning of our government,'' Senate Minority Leader Harry Reid said, adding that Trump is ''nothing more than a spoiled, unpatriotic drain on society who has earned nothing and helped no one." 
</p>

<p>
Trump was also taunted by Khizr Khan, the Gold Star parent who mocked the Republican nominee at the Democratic convention by asking him if he had read the US Constitution. ''I challenge Trump to take the naturalization test with me any day. His is demagoguery and pandering for vote. A divider like Trump can never be the steward of this country,'' Khan told Huffington Post. 
</p>


<p>
In his speech, Trump pressed on with his proposals to corral dissonant foreigners, pledging, ''Only those who we expect to flourish in our country - and to embrace a tolerant American society - should be issued visas.''</p>
<p>
In fact, the Trump effect will be felt even before the TITMUS papers are drawn up.
</p>
<p>
The Republican candidate said as soon as he takes office (if he wins), he will ask the State Department and the Department of Homeland Security to identify a list of regions where adequate screening cannot take place and direct them to ''stop processing visas from those areas until such time as it is deemed safe to resume based on new circumstances or new procedures.'' 
</p>

<p>
''The size of current immigration flows are simply too large to perform adequate screening. We admit about 100,000 permanent immigrants from the Middle East every year. Beyond that, we admit hundreds of thousands of temporary workers and visitors from the same regions. If we don't control the numbers, we can't perform adequate screening,'' he said, in an indication that even temporary workers and visitors could be affected.
</p>

<p>
Trump also spoke of setting up a commission whose goal will be to ''identify and explain to the American public the core convictions and beliefs of Radical Islam, to identify the warning signs of radicalization, and to expose the networks in our society that support radicalization.'' This commission, he said, will be used to develop new protocols for local police officers, federal investigators, and immigration screeners. 
</p>

<p>
''To accomplish a goal, you must state a mission: the support networks for Radical Islam in this country will be stripped out and removed one by one. Immigration officers will also have their powers restored: those who are guests in our country that are preaching hate will be asked to return home,'' he warned.</p>

<p>
He also pledged to keep open Guantanamo Bay, with a renewed emphasis on human intelligence, and said drone strikes will remain part of the US strategy, although it will also seek to capture high-value targets to gain needed information to dismantle their organizations. 
</p>

<p>
Trump did not identify the countries that would face the immigration and visa scrutiny beyond broadly invoking the middle-east region, although he cited honor killings in Pakistan and said speaking out against the horrible practice would be a litmus test to determine moderate Muslims, who he said his administration would befriend.
</p>

<p>
Many Indian Trump supporters believe his proposals will be aimed only at ''radical Islam'' although their sweeping nature — including the broad pledge to immediately suspend immigration from all terror-prone regions — suggests a broader squeeze going down on a slippery slope. 

</p>

		

