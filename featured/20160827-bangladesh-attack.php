<h2>Bangladesh cops storm militant hideout, kill 'mastermind' of Dhaka cafe attack</h2>

<p class="details">Aug 27,2016,10.44 AM IST <a href="#"></a> </p>

<blockquote>Bangladesh: The Dhaka cafe attack was the worst terror strike in the <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login"> country that has been battling rising extremism.</a> </blockquote>

<p><img class="alignright" src="/images/bangla.png" width="350" height="350" alt="Bangladesh" /></p>
<p>
Bangladeshi security forces killed four Islamist militants, including the mastermind of an attack on a cafe in Dhaka+ last month in which 22 people, mostly foreigners, were killed, the head of police counter-terrorism in the capital said on Saturday.
</p> <p>
The militants were killed in a raid on their hideout in Naraynganj, outskirts of Dhaka, Moniru Islam told Reuters. Bangladesh-born Canadian citizen Tamim Ahmed Chowdhury+ , the alleged mastermind of the attack on the cafe, was among those killed, he said.
</p> <p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three">
Tamim Chowdhury </a> had returned to Bangladesh from Canada three years ago. He had since led and financed efforts to radicalise young Muslims
</p> <p>
Five assailants stormed an upscale cafe in Dhaka's Gulshan neighbourhood on July 1 and killed 20 hostages, including 18 foreigners in Bangladesh's deadliest single militant attack. 

</p> <p>
<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login"> 
The siege was by far the deadliest in </a> a string of attacks claimed by Islamist groups which have blighted Bangladesh over the last three years.

</p> <p>
The Islamic State organisation claimed responsibility+ and the gunmen were pictured posing with IS flags in images posted on a website affiliated to the group.

</p> <p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three">
Prime Minister Sheikh Hasina's government </a> had said however that the attack was the work of a homegrown Islamist group, insisting international jihadist networks have not got a foothold in Bangladesh. 
</p> <p>

Dozens of foreigners, activists and members of minority religious groups have been killed by Islamists in Bangladesh over the last three years.

</p> <p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three">
Many of them, including victims of the cafe attack</a>, have been hacked to death with makeshift machetes.
</p>




