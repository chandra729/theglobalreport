<h2>Colombia ceasefire to end half-century war in hours</h2>

<p class="details">AFP | Aug 29, 2016, 07.18 AM IST<a href="TimesofIndia"></a> </p>


<p>
<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank" >
A historic midnight ceasefire on Sunday </a> is set to end a 52-year-old war between the Colombian state and FARC rebels.
Hundreds of thousands of Colombians have died since 1964 as rebel armies and gangs battled in the jungles in what is considered Latin America's last major civil armed conflict.
</p>

<p><img class="alignright" src="/images/colombia.png" width="350" height="350" alt="colombia" /></p>
 <p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">The Revolutionary Armed Forces of Colombia (FARC) </a> on Sunday issued the order for its fighters to observe the cease fire from midnight (0500 GMT Monday).
</p> <p>

"I order all our commanders and units and each one of our combatants to definitively cease fire and hostilities against the Colombian state from midnight tonight" top FARC leader Timoleon Jimenez said in a declaration before the media in Cuba, where peace talks were held.
</p> <p>
On the government side, President Juan Manuel Santos on Thursday ordered the Colombian national armed forces to halt military operations against the FARC.
</p> <p>
<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> 
"We noted with excitement the president's </a> order to the army. Consequently we are proceeding to give the same order to our troops," Jimenez said Sunday, surrounded by FARC commanders in white shirts.
</p> <p>
Santos wrote on Twitter: "The end of the conflict has arrived!"
</p> <p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">The FARC declared a unilateral ceasefire in July 2015 </a>. But Sunday night's ceasefire is the first in which both sides have committed to a definitive end to the fighting.
</p> <p>
"The ceasefire is really one more seal on the end of the conflict. It is the test of fire," said Carlos Alfonso Velazquez, a security expert at the University of La Sabana.
</p> <p>
Santos and Timochenko are due to sign a final, full peace agreement sometime between September 20 and 26.
</p> <p>
The ceasefire and definitive end of hostilities will be followed by a six-month demobilization process.
</p> <p>
From Monday, the FARC's estimated 7,500 fighters will start heading to collection points to give up their weapons under UN supervision.
</p> <p>

<a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> Guerrillas who refuse to demobilize and disarm </a>"will be pursued with all the strength of the state forces," Santos told El Espectador newspaper.
</p> <p>

Before the demobilization, the FARC will convene its leaders and troops one last time before transforming into "a legal political movement," according to a statement published on Saturday.
</p> <p>
On October 2, Colombians will go to the polls to cast ballots in a referendum that Santos hopes will endorse the peace agreement.
</p> <p>
"A victory for the 'Yes' vote will be a mandate from citizens for future governments," Santos was quoting as saying by El Espectador.
"The plebiscite will grant the political legitimacy that is needed."
</p> <p>
<a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" >
He said the exact question that will be posed to voters in the referendum would be announced "in the coming days."</a>
</p> <p>
"We are on the verge of perhaps the most important political decision of our lives," Santos said in a speech on Saturday.
</p> <p>
The territorial and ideological conflict has drawn in various left- and right-wing armed groups and gangs.
</p> <p>
It has left some 260,000 dead, 45,000 missing and 6.9 million people uprooted from their homes.
</p> <p>
Efforts to launch peace talks with a smaller rebel group, the National Liberation Army, have yet to bear fruit.
</p> <p>
<a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">
But with the country's biggest rebel group, the FARC, </a> ordering a definitive ceasefire, the conflict appears to be reaching an end.
</p> <p>

"To the soldiers, naval personnel and air force pilots, police and state security and intelligence agencies, we wish to express our clear and definite will for reconciliation," said Jimenez, known by the nom-de-guerre Timochenko, in Havana.
</p> <p>

"Rivalries and resentment must remain in the past. Today more than ever we regret that so much death and pain has been caused by the war.
</p> <p>
Today more than ever we wish to embrace them as compatriots and start to work together for a new Colombia."
</p>



