<h2>IS running terror classes in Kerala, Yasmin Ahmed tells NIA</h2>

<p class="details">Munish Pandey| Aug 30,2016,08.20 AM IST<a href="TimesofIndia"></a> </p>

<blockquote><b>Kerala woman arrested <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank" > while on her way to Kabul reveals jihadi lessons were delivered under </a> the guise of sermons.</b></blockquote>
<p>
A radicalized Kerala schoolteacher who was arrested at Delhi airport+ on August 2 before she could board a flight to Kabul has revealed that the extremist group Daesh is now running "terror classes" in the country.
</p> 
  <p><img class="alignright" src="/images/kerala.png" width="350" height="350" alt="Kerala" /></p>
 
<p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> Forty young men and women have already been indoctrinated </a> there by fugitive Daesh recruiter Abdul Rashid, who persuaded Mumbai graduate Ashfaq Abdul Majid to join the group, which controls swathes of territory in Iraq and Syria. Ashfaq is believed to have left India on June 2.
</p> <p>

<a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank">The information provided by schoolteacher Yasmin Ahmad </a>, 29, has deepened counter-terror agencies' worries as it confirms the growing perception that Daesh has overtaken Pakistani outfits as the biggest threat to national security.
</p> <p>

<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> 
Inspector General Alok Mittal of the National Investigation Agency (NIA) </a> had told Mirror in an exclusive chat last week that Daesh's influence in the country had alarmed authorities. Nearly 40 people — suspected Daesh operatives and sympathizers — have been arrested in different parts of the country since mid-2014 and eight jihadist modules have been broken up.
</p> <p>
<a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">Yasmin is a native </a> of Bihar and moved to Kerala three years ago. She took up a job at Peace International School in Malappuram, where she met Abdul, according to an NIA officer.
</p> <p>
Abdul, from Kerala's Kasaragod district </a>, was also working as a teacher there. He is a prime suspect in the disappearance of 21 youths from the state, and is believed to have fled to Afghanistan.
</p> <p>
Yasmin was arrested by a special team of the Kerala police.
</p> <p>
<a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" >
"She has made some shocking revelations </a>, which are being verified. She claims 40 people were indoctrinated in jihadist ideology at secret camps in Kerala," the officer said.

</p> <p>

"We have identified some of the people who attended the sessions conducted by Abdul in Trikaripur, a town in northern Kerala. We monitoring their movements."
</p> <p>
<a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">
The NIA has moved an application before a Kochi court seeking Yasmin's remand </a>.
</p>

