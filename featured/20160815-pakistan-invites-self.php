<h2>Pakistan invites self-exiled Balochistan leaders for talks</h2>
       


<p>Days after Prime Minister Narendra Modi spoke about the atrocities being carried out on the people of Balochistan and Pakistan-occupied Kashmir 
(PoK),<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login">Pakistan has invited self-exiled Baloch leaders</a> for talks while stating that dialogue is the only way to find a solution to all issues. for talks while stating that dialogue is the only way to find a solution to all issues.</p>
        
<p><img class="alignright" src="/images/pak-invite.png" width="250" height="250" alt="IronSharmila" /></p>
<p>Balochistan chief minister Nawab Sanaullah Zehri and commander of southern command Lieutenant General Aamir Riaz said that they would welcome Baloch leaders' return to the country, Dawn reported. The invite was extended at the national flag-hoisting ceremony at the Quaid-i-Azam Residency in Ziarat, a district in Balochistan, on the occasion of the 70th Independence Day celebrations of Pakistan on Sunday.
</p>

<p> "It will be their choice to join national politics or do politics on a nationalist basis... We will honour it if the people of Balochistan give you the mandate," Dawn quoted him as saying. Zehri also made it clear that it would not be possible for the government to accept such leaders' ideology at gunpoint. "We will not allow anybody to impose his ideology by force...We have been the custodians of Balochistan for the last 500 years and became part of Pakistan at our own will," he added.  </p>


<p> In a first for any Prime Minister in an August 15 address , Narendra Modi referred to human rights abuses in Balochistan+ and Pakistan-occupied Kashmir. </p>


<p>"The world is watching. People of Balochistan, Gilgit, Baltistan and Pakistan-occupied Kashmir have thanked me a lot in the past few days. I am grateful to them," Modi said, referring to his comments last week on excesses in Pakistan's Balochistan and PoK.
</p>


<p>He said the way people from these Pakistani regions "wished me well, gives me great joy".


In thanking an Indian Prime Minister, "they have thanked the whole population of my country", he said. "I want to offer my gratitude to these people." </p>
<p>Modi had said at a meeting on Kashmir last week that it was time for Islamabad to explain to the world its "atrocities on people in Pakistan-occupied Kashmir and Balochistan". </p>

<p>
On Monday, Modi also hit out at Pakistan for supporting terrorism. This, he said, was in contrast to the way Indians reacted with sorrow when terrorists slaughtered school children in Peshawar.
</p>		
