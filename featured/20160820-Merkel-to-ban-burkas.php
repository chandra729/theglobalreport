<h2>Merkel to ban burkas </h2>

<p class="details">Published:20th Aug 2016 08:30AM|Last Updated:20th Aug 2016 08:30 AM <a href="#"></a> </p>
<!--<p class="details"> Published: 20th August 2016 08:30 AM|Last Updated: 20th August 2016 08:30 AM <a href="#">By The Daily Telegraph</a> </p> -->

<p>
Germany could impose a ban on women wearing burkas or full-face Islamic veils at schools<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login">and universities and while driving, under proposals announced by Angela Merkel's party. </a>  and universities and while driving, under proposals announced by Angela Merkel's party.
</p>
       
<p><img class="alignright" src="/images/merkel.png" width="250" height="250" alt="merkel" /></p>

<p>The burka "does not belong in our cosmopolitan country", Thomas de Maiziere, the interior minister and one of Mrs Merkel's closest allies, said as he announced the policy yesterday (Friday).</p>
<p>
"We all reject the full veil - not only the burka but also other types of full veil that only leave the eyes visible. They have no place in our society." The proposals stop short of the complete ban called for by senior figures in Mrs Merkel's Christian Democrat party (CDU). Instead what has been described as a "burka ban lite" will also apply to women working as public officials, attending public demonstrations, and in court.
</p><p>
"Showing your face is essential for our communication, co-existence and social cohesion and that's why we're asking everyone to show their faces," Mr de Maiziere said.
</p>
<p>
 "We want to introduce a law to make people show their faces and that means that those who break that law will have to face the consequences." Mrs Merkel made her own feelings on the issue clear in an interview with a number of German newspapers in advance of the announcement. "In my view, a fully veiled woman has almost no chance of integrating successfully in German society," she said.
</p><p>

The new policy was agreed at late night talks between Mr de Maiziere and interior ministers from several of Germany's 16 federal states where the CDU is in power.
</p><p>
Before it can become law it will have to win the support of Mrs Merkel's coalition partners in the federal government, the Social Democrats (SPD), where it has already run into opposition from senior ministers.
</p><p>
Andrea Nahles, the employment and social affairs minister, described the proposed ban as a sign of "increasingly xenophobic" debate in Germany and said it would set back attempts to integrate immigrants. Heiko Maas, the justice minister, called for the burka to be kept separate from security issues.
</p>
<p>
Mr de Maiziere said the CDU ministers had agreed to drop demands for a complete ban because it would almost certainly have been struck down by Germany's courts as unconstitutional. The policy is likely to be largely symbolic as, unlike in other European countries which have imposed bans, burkas and full-face veils are already extremely rare in Germany.
</p><p>
There are around four million Muslims in Germany, about 5 per cent of the population. A study in 2009 found more than two thirds of Muslim women in Germany wear no hair or face covering.
</p><p>
Calls for a burka ban have been largely led by the CDU ministers of two regions holding elections next month - Mecklenburg-Vorpommern and Berlin - where the party is under pressure from the Right-wing anti-immigration Alternative for Germany party.
</p>



