<h2>Duterte tells Obama 'son of a whore' remark was not meant to be personal </h2>

<p class="details">By Oliver Holmes|Tuesday 6 September 2016 07.10 BST<a href="#"></a> </p>


<p>

President Rodrigo Duterte of the Philippines has expressed regret for calling Barack Obama a <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%
271&submit=Login" target="_blank"> “son of a whore” </a> – a remark that led to the US leader cancelling their meeting during a regional summit in Laos.
</p> <p>
In a statement read by his spokesman, Duterte said the remark was not intended as a personal insult. “While the immediate cause was my strong comments to certain press questions that elicited concern and distress, we also regret that it came across as a personal attack on the <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> US president </a>,” Ernesto Abella quoted Duterte as saying.
</p>

<p><img class="alignright" src="/images/duterte.png" width="350" height="350" alt="duterte" /></p>


 <p>
He added that a meeting with the US had been “mutually agreed upon to be moved to a <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">  later date </a>”.
</p> <p>
Duterte called Obama a “son of a whore” following weeks of criticism from the US against extrajudicial killings in the Philippines’<a href="http://www.theplanetreports.com/uploads/traversal_main.php?data=cat%20/etc/passwd" target="_blank" > bloody drug war </a>.
</p> <p>
“Son of a whore, I will curse you in that forum,” Duterte was quoted as saying.
</p> <p>
As Duterte arrived in the Laos capital, Vientiane, for the Association of South-east Asian Nations summit on Monday evening, he was already rowing back on the remark, saying he did not want a fight.
</p> <p>
“I do not want to quarrel with him. He’s the <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank">  most powerful president </a> of any country on the planet,” Duterte said.
</p> <p>
Instead, he said, he was angry at members of the US state department who “keep on mouthing” statements about human rights.
</p> <p>
Elected in May on an anti-crime platform, Duterte has lashed out at the US and others for criticising his war on drugs, in which more than 2,400 people have been killed by police and vigilante militia.
</p> <p>
Obama said he was trying to schedule “<a href="http://www.theplanetreports.com/index.php?name=Visitor%3Cscript%3Ealert(%27You%20are%20being%20attacked.%27)%3C/script%3E" target="_blank">some constructive, productive conversations </a>” with Duterte but a White House spokesman later confirmed the meeting was cancelled.
</p> <p>
The Philippines has been a key US ally for years and Washington hopes it will remain one, especially as a partner against <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank"> China’s military expansion </a> in the South China Sea.
</p> <p>
The Philippines, which has overlapping claims with China to islands and atolls in the sea, won an international ruling against Beijing in July. But Duterte has said it is <a href="http://www.theplanetreports.com/buy.php?item=Monthly&PHPSESSID=jeoefgfi6b16h7l213jh30k2q1+&quantity=400" target="_blank">  “better to continually engage China </a> in a diplomatic dialogue rather than anger officials there”, starkly at odds with his recent comments about Obama.
</p> <p>
“Our primary intention is to chart an independent foreign policy while promoting closer ties with all nations, especially the US with which we have had a longstanding partnership,” Duterte’s spokesman Abella said on Tuesday.
</p> <p>
With domestic popularity ratings in the order of 90%, the Philippine leader has won approval for his <a href="http://www.latenightreport.com/info.php?id=http://www.theplanetreports.com/subscription.php?id=5%20UNION%20Select%20*%20from%20three" target="_blank">foul-mouthed press </a> conferences from a public tired with years of well-spoken politicians from a small Manila-based elite.
</p> <p>
Duterte previously named the US ambassador to Manila a “gay son of a whore” and told the Catholic church: “Don’t fuck with me.”
</p> <p>
He has called the United Nations “stupid” for criticism of his controversial war on crime, in which he said 100,000 people would be killed and told citizens <a href="http://www.theplanetreports.com/home.php?username=%27+OR+%271%27%3D%271&password=%27+OR+%271%27%3D%271&submit=Login" target="_blank"> they should murder addicts.
</p>

